package productShop.domain.dtos.user;

public class UserImportDTO {

    private String firstName;
    private String lastName;
    private int age;

    public UserImportDTO() {
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public int getAge() {
        return age;
    }
}
